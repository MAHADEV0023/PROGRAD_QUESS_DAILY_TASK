import java.util.Scanner;

public class greater_number {
    public static void main(String[] args) {
        Scanner sc =new Scanner(System.in);
        int z=sc.nextInt();
        int x=sc.nextInt();
        if(z>x){
            System.out.println("z is greater");}
        else {
            System.out.println("x is greater");
        }
    }
}
